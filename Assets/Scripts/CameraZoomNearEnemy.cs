using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class CameraZoomNearEnemy : MonoBehaviour
{
	CameraScroll _cameraControler;
	public float _circleRadius,_zoomMagnitude;
	LayerMask _enemyLayer;

	private void Start()
	{
		_cameraControler = FindObjectOfType<CameraScroll>();
	}
	private void Update()
	{
		Collider2D[] enemiesColliders = Physics2D.OverlapCircleAll(transform.position, _circleRadius, _enemyLayer);
		if (enemiesColliders.Length!=0)
		{
			_cameraControler.Zoom(_zoomMagnitude);
		}
		else
		{
			_cameraControler.DeZoom(0.5f);
		}
	}
}
